class HourClock:

    def __init__(self, hours):
        self.hours = hours

    def get_hours(self):
        return self._hours

    def set_hours(self, n):
        if isinstance(n, int) and n in range(1, 13):
            self._hours = n
        else:
            raise ValueError('–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–µ –≤—Ä–µ–º—è')

    hours = property(get_hours, set_hours)



